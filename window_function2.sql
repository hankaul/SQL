WHERE JOB='SALESMAN';



--[10] RATIO_TO_REPORT (51)
SELECT ENAME,JOB,SAL,
			ROUND(RATIO_TO_REPORT(SAL)
			OVER(),2)AS RATIO_TO_REPORT 
		FROM EMP
		WHERE JOB='SALESMAN'
		ORDER BY SAL -- RATIO_TO-REPORT



--[11] PERCENT_RANK
SELECT DEPTNO,ENAME,SAL,
	PERCENT_RANK()
	OVER(PARTITION BY DEPTNO
	ORDER BY SAL DESC) 
	AS PR
FROM EMP;


--[12] CUME_DIST \
SELECT DEPTNO,ENAME,SAL,
	ROUND(CUME_DIST()
	OVER(PARTITION BY DEPTNO
	ORDER BY SAL DESC),2)
	AS CD
FROM EMP



--[13] NTILE(43)
SELECT ENAME,SAL,
	NTILE(4) OVER(ORDER BY SAL DESC) AS TILE
FROM EMP;