--ROW_NUMBER
SELECT ename,sal,
	ROW_NUMBER ()
	OVER(ORDER BY SAL DESC) 
FROM EMP



--[6] FIRST_VALUE
SELECT DEPTNO,ENAME,SAL,
	FIRST_VALUE(ENAME)
	OVER(PARTITION BY DEPTNO
	ORDER BY SAL DESC 
	ROWS UNBOUNDED PRECEDING ) AS ENAME_FV
FROM EMP



--[7] LAST_VALUE 
SELECT DEPTNO,ENAME,SAL,
	LAST_VALUE(ENAME)
	OVER(PARTITION BY DEPTNO
	ORDER BY SAL DESC
	ROWS BETWEEN CURRENT ROW AND UNBOUNDED FOLLOWING )
	AS ENAME_FV
FROM EMP



--[8] LAG (46)
